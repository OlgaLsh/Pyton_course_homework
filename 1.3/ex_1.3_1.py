# Двухмерный список символов

letters = ['А','Б','В', 'Г', 'Д', 'Е', 'Ё', 'Ж', 'З', 'И', 'К', 'Л', 'М', 'Н', 'О', 'П', 'Р', 'С', 'Т', 'У', 'Ф', 'Х', 'Ц', 'Ч', 'Ш', 'Щ', 'Ъ', 'Ы', 'Ь', 'Э', 'Ю', 'Я']
a = 1
for i in range (len(letters)):
    for i in range (a):
        print(letters[i], end=" ")
    a += 1
    print()
